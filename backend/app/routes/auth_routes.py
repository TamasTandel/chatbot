# Standard error handlers for JWT (optional, but good for consistent API responses)
@jwt.expired_token_loader
def expired_token_callback(jwt_header, jwt_payload):
    return jsonify({"msg": "Token has expired"}), 401

@jwt.invalid_token_loader
def invalid_token_callback(error_string):
    return jsonify({"msg": "Invalid token", "error": error_string}), 422 # Unprocessable Entity

@jwt.unauthorized_loader
def missing_token_callback(error_string):
    return jsonify({"msg": "Authorization token is missing", "error": error_string}), 401
